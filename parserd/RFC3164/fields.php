require_once load_once("parserd/filter/$model", 'dictionary.php');


function set_fields(&$fields, &$records) {
	global $timezone;

    $extract_ipv4_pattern = "(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)\\.(?:25[0-5]|2[0-4][0-9]|[01]?[0-9][0-9]?)";
    $extract_ipv6_pattern = "(([0-9a-fA-F]{1,4}:){7,7}[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,7}:|([0-9a-fA-F]{1,4}:){1,6}:[0-9a-fA-F]{1,4}|([0-9a-fA-F]{1,4}:){1,5}(:[0-9a-fA-F]{1,4}){1,2}|([0-9a-fA-F]{1,4}:){1,4}(:[0-9a-fA-F]{1,4}){1,3}|([0-9a-fA-F]{1,4}:){1,3}(:[0-9a-fA-F]{1,4}){1,4}|([0-9a-fA-F]{1,4}:){1,2}(:[0-9a-fA-F]{1,4}){1,5}|[0-9a-fA-F]{1,4}:((:[0-9a-fA-F]{1,4}){1,6})|:((:[0-9a-fA-F]{1,4}){1,7}|:)|fe80:(:[0-9a-fA-F]{0,4}){0,4}%[0-9a-zA-Z]{1,}|::(ffff(:0{1,4}){0,1}:){0,1}((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])|([0-9a-fA-F]{1,4}:){1,4}:((25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9])\\.){3,3}(25[0-5]|(2[0-4]|1{0,1}[0-9]){0,1}[0-9]))";

	$fields ['rawlog'] = $records ['rawlog'];
	$fields ['Type'] = 'generic';
	$fields ['subtype'] = 'RFC3164';

  //-------------------------
  //- HOSTNAME
  //-------------------------
  if (!empty ($records['orig'])) {
    $fields['hostname'] = str_replace(':','',$records['orig']);
  }

	//-------------------------------
	//----     Date
	//-------------------------------
	if (empty($records['timestamp'])) {
	  $fields['Date'] = date('Y-m-d\TH:i:s');
	} else if (!empty($records['date']) && !empty($records['time'])){
	  $fields['Date'] = $records['date'] . 'T' . $records['time'];
	}
	$fields['Date'] .= $timezone;

}